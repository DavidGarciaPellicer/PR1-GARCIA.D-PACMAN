       private void refrescarMatriz()
       {
           for (int i = 0; i < width; i++)
           {
               for (int j = 0; j < height; j++)
               {
                   matrizPantalla[i + posX, j + posY] = pacMan.ASCIIModel[inicioY, inicioX];
                   inicioX++;
               }
               inicioY++;
               inicioX = 0;
           }

           inicioX = 0;
           inicioY = 0;
       }

